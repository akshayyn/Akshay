    private void areaComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_areaComboActionPerformed
        if (null != areaCombo.getSelectedItem()) {
            Area area = (Area) areaCombo.getSelectedItem();
            if (null != area) {
                streetCombo.removeAllItems();
                for (Street street : area.getStreetMap().getStreetList()) {
                    streetCombo.addItem(street);
                }
            }
    }//GEN-LAST:event_areaComboActionPerformed
    }
    private void streetComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_streetComboActionPerformed
        if (null != streetCombo.getSelectedItem()) {
            Street street = (Street) streetCombo.getSelectedItem();
            if (null != street) {
                plotCombo.removeAllItems();
                for (Plot plot : street.getPlotList()) {
                    plotCombo.addItem(plot);
                }
            }
        }
    }//GEN-LAST:event_streetComboActionPerformed

