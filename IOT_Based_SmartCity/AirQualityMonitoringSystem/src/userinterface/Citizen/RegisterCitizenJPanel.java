    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
        if(null != cityCombo.getSelectedItem()){
            City city = (City) cityCombo.getSelectedItem();
            if(null != areaCombo.getSelectedItem()){
                Area area = (Area) areaCombo.getSelectedItem();
                Citizen citizen = area.getCitizenDirectory().addCitizen();
                citizen.getPerson().setFirstName(firstNameField.getText());
                citizen.getPerson().setLastName(lastNameField.getText());
                 DateFormat dateFormat = new SimpleDateFormat("MM/dd/yyyy");
                  dateFormat.setLenient(false);
                 Date date = null;
                  try {
                    date = dateFormat.parse(dobField.getText());
                } catch (ParseException ex) {
                    Logger.getLogger(RegisterCitizenJPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
                citizen.getPerson().setDateOfBirth(date);
                citizen.setEmailId(emailField.getText());
                citizen.setPhoneNumber(areaCodeField.getText()+numberField.getText());
                citizen.setResidence((House)aptCombo.getSelectedItem());
                citizen.getResidence().getResidents().add(citizen);
                JOptionPane.showMessageDialog(null, "Step 1 done! You will be redirected to next page");
                RegisterMedicalRecordJPanel registerMedicalRecordJPanel = new RegisterMedicalRecordJPanel(userPanel,area, citizen);
        userPanel.add("registerMedicalRecordJPanel", registerMedicalRecordJPanel);
        CardLayout layout = (CardLayout) userPanel.getLayout();
        layout.next(userPanel);
                
            }
        }
    }//GEN-LAST:event_createButtonActionPerformed

    private void dobFieldFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_dobFieldFocusGained
        if (dobField.getText() != null && (dobField.getText().equals("mm/dd/yyyy"))) {
            dobField.setText("");
        }
        dobError.setText("");
        dobField.setBackground(Color.white);
        if (errorCount == 0) {
            createButton.setEnabled(true);
        }
    }//GEN-LAST:event_dobFieldFocusGained

    private void dobFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_dobFieldFocusLost
        boolean validationResult = formValidator.verifyStringInput(dobField.getText());
        if (validationResult) {
            if (formValidator.verifyDateFormat(dobField.getText(), "MM/dd/yyyy")) {
                if ((dobError.equals(""))) {
                    errorCount--;
                }
                return;
            }
        }
        dobError.setText("Please Enter  a valid dob in mm/dd/yyyy format");
        dobField.setBackground(Color.pink);
        createButton.setEnabled(false);
    }//GEN-LAST:event_dobFieldFocusLost

